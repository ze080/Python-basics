# ===== DATA TYPES DEMO IN PYTHON =====

# 1️⃣ Integer (int): Whole numbers
my_int = 10
print(my_int)

# 2️⃣ Float: Decimal numbers
my_float = 3.14
print(my_float)

# 3️⃣ Complex: Numbers with real and imaginary part
my_complex = 2 + 3j
print(my_complex)

# 4️⃣ Boolean (bool): True or False
is_alive = True
print(is_alive)

# 5️⃣ String (str): Text
my_name = "Zayed"
print(my_name)

# 6️⃣ NoneType: Represents the absence of a value
nothing = None
print(nothing)

# type() function
# It's not a data type itself, it's a built-in function that returns the type of an object
# We have't seen functions yet, but still , it's worth mentioning here
print(type(my_int))  # Output: <class 'int'>
print(type(my_float))  # Output: <class 'float'>
print(type(my_complex))  # Output: <class 'complex'>
print(type(is_alive))  # Output: <class 'bool'>
print(type(my_name))  # Output: <class 'str'>
print(type(nothing))  # Output: <class 'NoneType'>
